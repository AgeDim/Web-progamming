package web.Lab3.bean;

public class Interim {
    private Float x;
    private Float y;
    private Float r;
    public Interim(Float x, Float y, Float r) {
        this.x = x;
        this.y = y;
        this.r = r;
    }
    public Float getX() {
        return x;
    }

    public void setX(Float x) {
        this.x = x;
    }

    public Float getY() {
        return y;
    }

    public void setY(Float y) {
        this.y = y;
    }

    public Float getR() {
        return r;
    }

    public void setR(Float r) {
        this.r = r;
    }
}
